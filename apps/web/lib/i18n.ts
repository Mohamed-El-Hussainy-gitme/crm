function validDate(value?: string | number | Date | null) {
  if (value === null || value === undefined || value === "") return null;
  const date = value instanceof Date ? value : new Date(value);
  return Number.isNaN(date.getTime()) ? null : date;
}

export function formatDateTime(locale: Locale, value?: string | number | Date | null, fallback?: string) {
  const date = validDate(value);
  if (!date) return fallback ?? translate(locale, "common.noDate");
  return new Intl.DateTimeFormat(localeTag(locale), {
    year: "numeric",
    month: "short",
    day: "numeric",
    hour: "numeric",
    minute: "2-digit",
  }).format(date);
}

export function formatDateOnly(locale: Locale, value?: string | number | Date | null, fallback?: string) {
  const date = validDate(value);
  if (!date) return fallback ?? translate(locale, "common.noDate");
  return new Intl.DateTimeFormat(localeTag(locale), {
    year: "numeric",
    month: "short",
    day: "numeric",
  }).format(date);
}

export function formatTimeOnly(locale: Locale, value?: string | number | Date | null, fallback?: string) {
  const date = validDate(value);
  if (!date) return fallback ?? translate(locale, "common.noDate");
  return new Intl.DateTimeFormat(localeTag(locale), {
    hour: "numeric",
    minute: "2-digit",
  }).format(date);
}

export function formatNumber(locale: Locale, value?: number | string | null, options?: Intl.NumberFormatOptions) {
  const numeric = typeof value === "string" ? Number(value) : Number(value ?? 0);
  if (!Number.isFinite(numeric)) return "0";
  return new Intl.NumberFormat(localeTag(locale), options).format(numeric);
}

export function formatCurrency(locale: Locale, value?: number | string | null) {
  const numeric = typeof value === "string" ? Number(value) : Number(value ?? 0);
  if (!Number.isFinite(numeric)) return "0";
  return new Intl.NumberFormat(localeTag(locale), {
    style: "currency",
    currency: "EGP",
    maximumFractionDigits: 0,
  }).format(numeric);
}
