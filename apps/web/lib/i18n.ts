export function resolveLocale(input?: string | null): Locale {
  return input === "ar" ? "ar" : "en";
}

export function localeTag(locale: Locale) {
  return locale === "ar" ? "ar-EG" : "en-US";
}

export function localeDir(locale: Locale) {
  return locale === "ar" ? "rtl" : "ltr";
}

function getPath(locale: Locale, key: string): string | null {
  const dictionary = dictionaries[locale];
  const parts = key.split(".");
  let current: DictionaryValue | Record<string, DictionaryValue> | undefined = dictionary;
  for (const part of parts) {
    if (!current || typeof current === "string") return null;
    current = (current as Record<string, DictionaryValue>)[part];
  }
  return typeof current === "string" ? current : null;
}

export function translate(locale: Locale, key: string, vars?: TranslationVars) {
  const template = getPath(locale, key) ?? getPath("en", key) ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars?.[name] ?? ""));
}

